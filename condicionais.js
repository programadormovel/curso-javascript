// Estrutura condicional em JavaScript
// Desvio condicional usando 'if' e 'else'
// Teste lógico  
// Estrutura de controle de fluxo
// Estrutura de decisão  

var idade = 15;

// Condicional simples
if(idade > 15){
    console.log("Pode votar!");
}

// Condicional composta
if(idade > 15){
    console.log("Pode votar!");
} else {
    console.log("Não pode votar ainda.");
}

// Condicional composta com operador lógico
if(idade > 15 && idade == 16){
    console.log("Pode votar!");
} else {
    console.log("Não pode votar ainda.");
}

if(idade >= 16) {
    console.log("Pode votar!");
} else {
    console.log("Não pode votar ainda.");
}